$(document).ready(function() {
    $('.drag').draggable({
      appendTo: 'body',
      helper: 'clone'
    });
    $('#dropzone').droppable({
      activeClass: 'active',
      hoverClass: 'hover',
      accept: ":not(.ui-sortable-helper)", // Reject clones generated by sortable
      drop: function (e, ui) {
        var $el = $('<div class="drop-item"><details><summary>' + ui.draggable.text() + '</summary></details></div>');
        $el.append($(' <div class="col-lg-2"><div class="input-group"> <span class="input-group-btn"><button type="button" class="quantity-left-minus btn btn-danger btn-number"  data-type="minus" data-field=""><span class="glyphicon glyphicon-minus"></span></button></span><input type="text" id="quantity" name="quantity" class="form-control input-number" value="10" min="1" max="100"><span class="input-group-btn"><button type="button" class="quantity-right-plus btn btn-success btn-number" data-type="plus" data-field=""><span class="glyphicon glyphicon-plus"></span></button></span></div> </div>').click(function () { $(this).parent().detach(); }));
        $(this).append($el);
      }
    }).sortable({
      items: '.drop-item',
      sort: function() {
        // gets added unintentionally by droppable interacting with sortable
        // using connectWithSortable fixes this, but doesn't allow you to customize active/hoverClass options
        $( this ).removeClass( "active" );
      }
    });
});



